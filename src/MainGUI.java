    private void bookingButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookingButtonActionPerformed

        Date date = Date.valueOf(bookingDateComboBox.getSelectedItem().toString());
        String flightName = bookingFlightComboBox.getSelectedItem().toString();
        String customerName = bookingCustomerComboBox.getSelectedItem().toString();
        BookingEntry newBookingEntry = new BookingEntry(customerName, flightName, date);
        BookingQueries data = new BookingQueries();
        WaitListQueries newWaitingListEntry = new WaitListQueries();
        WaitListQueries waitlistData = new WaitListQueries();

        if (data.checkSeatAvailable(data.getBookingSeats(newBookingEntry.getFlightName(), newBookingEntry.getDate()), data.getFlightseat(newBookingEntry.getFlightName()))) {

            WaitList newWaitlist = new WaitList(customerName,flightName, date);
            newWaitingListEntry.addCustomerToWaitList(newWaitlist.getCustomerName(), newWaitlist.getFlightName(), newWaitlist.getDate());

            ArrayList<WaitList> waitingListNameList = new ArrayList<>(waitlistData.getWaitingListCustomers(date));
            System.out.println(waitingListNameList.get(0));
            bookingResults.setText("The FLIGHT is full, you have been added to the waitlist!\n"
                + "Customer: " + newBookingEntry.getCustomerName() + "\nFlight: " + newBookingEntry.getFlightName() + "\nDate: "
                + newBookingEntry.getDate() + "\nWaitlist Position: " + waitingListNameList.size());
        } else {
            data.addCustomer(newBookingEntry.getCustomerName(), newBookingEntry.getFlightName(),newBookingEntry.getDate());
            bookingResults.setText("You have successfully BOOK the flight!\n"
                + "Customer: " + newBookingEntry.getCustomerName() + "\nFlight: " + newBookingEntry.getFlightName() + "\nDate: "
                + newBookingEntry.getDate());
        }
    }//GEN-LAST:event_bookingButtonActionPerformed

