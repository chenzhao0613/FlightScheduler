    private void checkCustomerStatusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkCustomerStatusButtonActionPerformed
        String customer = customerStatusComboBox.getSelectedItem().toString();
        Customer checkBookedCustomer = new Customer();
        Customer checkWaitlistCustomer = new Customer();
        ArrayList<BookingEntry> customerBookedFlight = new ArrayList<>(checkBookedCustomer.getBookedCustomer(customer));
        ArrayList<BookingEntry> customerWaitlistFlight = new ArrayList<>(checkWaitlistCustomer.getWaitlistCustomer(customer));

        if(customerBookedFlight.isEmpty() && customerWaitlistFlight.isEmpty()){
            statusResults.setText("No Customer Status!");
        }
        else{
            statusResults.setText("Customer:\tFlight:\tDate:\tStatus:\n");
            for (int i = 0; i < customerBookedFlight.size(); i++) {
                String flight = customerBookedFlight.get(i).getFlightName();
                Date date = customerBookedFlight.get(i).getDate();
                statusResults.append(customer + "\t" + flight + "\t" + date + "\t" + "Booked\n");
            }
            for (int j = 0; j < customerWaitlistFlight.size(); j++) {
                String waitlistFlight = customerWaitlistFlight.get(j).getFlightName();
                Date waitlistDate = customerWaitlistFlight.get(j).getDate();
                statusResults.append(customer + "\t" + waitlistFlight + "\t" + waitlistDate + "\t" + "Waitlisted\n");
            }
        }
    }//GEN-LAST:event_checkCustomerStatusButtonActionPerformed

